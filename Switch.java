
public class Switch {
		public static void main(String args[])
		{
			char ch='C';
			switch(ch) {
			case 'A':
				System.out.println("A,We have found A");
				break;
			case 'B':
				System.out.println("B,We have found B");
				break;
			case 'C':
				 System.out.println("C,We have found C");
				 break;
			case 'D':
				 System.out.println("D,We have found D");
				 break;
				 default:
					 System.out.println("Oops check the condition");
			}
		}
	}
